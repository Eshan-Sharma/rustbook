fn main() {
    println!("Hello, world! just printing data types");
    let x: f32 = 3.02; //f32
    println!("x is {}", x);

    let y = 4.01; //f64
    println!("y is {}", y);

    let sum = 5 + 10;
    println!("sum is {sum}");

    let diff = 34.2 - 21.02;
    println!("diff is {diff}");

    let quotient = 543.21 / 89.1;
    println!("quotient is {quotient}");
    let truncated = -1 / 9;
    println!("truncated is {truncated}");

    let remainder = 10 % 3;
    println!("remainder is {remainder}");
}
